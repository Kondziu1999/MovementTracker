package com.example.tracker;

public class LatLanHolder {

    double lat;
    double lan;

    public LatLanHolder(double lat, double lan) {
        this.lat = lat;
        this.lan = lan;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLan() {
        return lan;
    }

    public void setLan(double lan) {
        this.lan = lan;
    }
}
